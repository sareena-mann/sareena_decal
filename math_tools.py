#adds a and b
def add(a, b):
    return a + b

#subtracts b from a
def subtract(a, b):
    return a - b

# multiples a and b
def multiply(a, b):
    return a * b

#if b is zero, return zero; otherwise, it divides a by b
def divide(a, b):
    if (b == 0):
        return 0
    return a / b

